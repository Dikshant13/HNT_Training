function subtract(x,y){
    return x-y;
}

function ADD(x,y){
    return x+y;
}

let product = function(x,y){
    return x*y;
}

let square =function(x){
    return x*x;
}

console.log("subtract:",subtract(5,4));
console.log("Add:",ADD(5,5));
console.log("product:",product(3,4));
console.log("square:",square(5));